function openPage() {
    var x = document.getElementById("search").value;

    if (x === "start up") {
        window.open(href="startup.html");
    }

    else if (x === "ponyo") {
        window.open(href="ponyo.html");
    }

    else if (x === "squid game") {
        window.open(href="Squid%20Game.html");
    }

    else if (x === "weathering with you") {
        window.open(href="Tenki%20no%20Ko.html");
    }

    else if (x === "tenki no ko") {
        window.open(href="Tenki%20no%20Ko.html");
    }

    else {
        window.open(href="belum.html");
    }
}